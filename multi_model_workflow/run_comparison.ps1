$files = @(
  @{ Model = "Codex";  Stage = "Blind attempt";       Path = "generated_proofs/codex/agnostic_attempt_1.lean" },
  @{ Model = "Codex";  Stage = "Repaired attempt";    Path = "generated_proofs/codex/agnostic_attempt_2_repaired.lean" },
  @{ Model = "Codex";  Stage = "Simplified attempt";  Path = "generated_proofs/codex/simplified_attempt.lean" },
